int bigMod(ll base, ll p)
{
    if(p == 0) return 1;
    ll ret = bigMod(base, p/2);
    ret = (ret*ret);
    if(p&1)
    {
        ret *= base;
    }
    return ( ret%100 );
}



int main()
{
    ll sum;

    int kases;
    ll K, N;
    cin>>kases;
    while(kases--)
    {
        cin>>K>>N;
        ll times = K/100;
        ll remain = K % 100;

        sum = 0;


        if(times > 0)
        {
            for(int i=1; i<100; i++)
            {
                sum += bigMod(i, N);
            }
        }

        sum = ((sum%100)*times)%100;

        for(int i=1; i<=remain; i++)
        {
            sum += bigMod(i, N);
        }

        sum = sum%100;

        if(sum < 10)
        {
            cout<<0<<sum;
        }
        else
        {
            cout<<sum;
        }
        cout<<endl;


    }




}
