void solve(int n, int p)
{
    vector<int>graph[30];

    for(int i=1; i<=5; i++)
    {
        for(int j=1; j<i; j++)
        {
            graph[j].pb(i);
        }
    }

    for(int i=6; i <= n; i++)
    {
        graph[i].pb(i-1);
        graph[i].pb(i-2);

        for(int j=i-3; j>0 && p > 0; j--, p--)
        {
            graph[i].pb(j);
        }
    }


    for(int i=1; i<=n; i++)
    {
        loop(j, SZ(graph[i]))
        {
            cout<<i<<" "<<graph[i][j]<<endl;
        }
    }



}





