#define MAXX 2007


#define SQ(x) ( (x)*(x) )

ll getDist(pall A, pall B)
{
    return SQ(A.fr - B.fr) + SQ(A.sc - B.sc);
}


pall pos[MAXX];
int N;


ll solve()
{
    map<ll, int>mymap;

    ll cnt = 0;

    ll k;


    loop(i, N)
    {
        mymap.clear();

        loop(j, N)
        {
            mymap[ getDist(pos[i], pos[j]) ]++;
        }

        for(map<ll, int>::iterator it = mymap.begin(); it != mymap.end(); it++)
        {
            k = it->sc;

            cnt += ( (k*(k-1))/2 );
        }
    }

    return cnt;


}



