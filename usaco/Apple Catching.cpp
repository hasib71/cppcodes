#define MAXX 1007

int T, W;

int ara[MAXX];

int dp[MAXX][37][2];



int rec(int pos, int remain, int tree)
{
    if(pos >= T)
    {
        return 0;
    }
    else
    {
        int &ret = dp[pos][remain][tree];

        if(ret != -1) return ret;

        ret = 0;

        if(remain == 0)
        {
            ret = (tree == ara[pos]) + rec(pos+1, remain, tree);
        }
        else
        {

            ret = (tree == ara[pos]) + max(rec(pos+1, remain, tree), rec(pos+1, remain-1, !tree));
        }

        return ret;
    }
}


