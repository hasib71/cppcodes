vector<int>A, B, C, D;





int main()
{
    int n;

    cin>>n;

    A.resize(n), B.resize(n), C.resize(n), D.resize(n);


    loop(i, n)
    {
        cin>>A[i]>>B[i]>>C[i]>>D[i];
    }

    vector<int>v;

    loop(i, n)
    {
        loop(j, n)
        {
            v.pb(A[i] + B[j]);
        }
    }

    sort(all(v));



    ll cnt = 0;



    loop(i, n)
    {
        loop(j, n)
        {
            int s = C[i] + D[j];
            s = -s;

            int low = 0, high = SZ(v) - 1;

            while(low <= high)
            {
                int mid = (low+high)/2;


                if(v[mid] < s )
                {
                    low = mid + 1;
                }
                else
                {
                    high = mid - 1;
                }
            }


            while(v[low] == s)
            {
                cnt++;
                low++;
                if(low >= SZ(v)) break;
            }

        }
    }


    cout<<cnt<<endl;




}
