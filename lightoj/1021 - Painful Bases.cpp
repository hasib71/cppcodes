#define MAXX 30


int base, k;

string str;

int finalState;

ll dp[1<<17][20];


ll rec(int state, int r)
{

    ll &ret = dp[state][r];

    if(ret != -1) return ret;


    if(state == finalState)
    {
        if(r == 0)
        {
            return ret = 1;
        }
        else
        {
            return ret = 0;
        }
    }


    ret = 0;

    loop(i, SZ(str))
    {
        if( (state & (1<<i)) == 0 )
        {
            ret += rec(state | (1<<i), (r*base + str[i])%k );
        }
    }

    return ret;




}







ll solve()
{
    loop(i, SZ(str))
    {
        if(str[i] <= '9')
        {
            str[i] = str[i] - '0';
        }
        else
        {
            str[i] = 10 + str[i] - 'A';
        }
    }


    mem(dp, -1);

    finalState = (1<<SZ(str)) - 1;

    return rec(0, 0);

}



int main()
{
    int kases, kaseno = 0;

    sf("%d", &kases);

    while(kases--)
    {
        sf("%d %d", &base, &k);
        cin>>str;

        pf("Case %d: %lld\n", ++kaseno, solve());
    }




    return 0;
}
